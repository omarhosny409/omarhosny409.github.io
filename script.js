const root = document.documentElement;
const prefersReducedMotion = window.matchMedia('(prefers-reduced-motion: reduce)').matches;

const getSaved = (key, fallback) => {
  try {
    return localStorage.getItem(key) || fallback;
  } catch (error) {
    return fallback;
  }
};

const saveValue = (key, value) => {
  try {
    localStorage.setItem(key, value);
  } catch (error) {}
};

const getCurrentLang = () => root.dataset.lang === 'ar' ? 'ar' : 'en';
const getCurrentTheme = () => root.dataset.theme === 'light' ? 'light' : 'dark';

const translate = (key) => {
  const lang = getCurrentLang();
  return translations[lang][key] || translations.en[key] || key;
};

const applyLanguage = (lang) => {
  const safeLang = lang === 'ar' ? 'ar' : 'en';
  root.dataset.lang = safeLang;
  root.lang = safeLang;
  root.dir = safeLang === 'ar' ? 'rtl' : 'ltr';
  saveValue('lang', safeLang);

  document.title = translations[safeLang].metaTitle;
  const metaDescription = document.querySelector('meta[name="description"]');
  if (metaDescription) {
    metaDescription.setAttribute('content', translations[safeLang].metaDescription);
  }

  document.querySelectorAll('[data-i18n]').forEach((element) => {
    element.textContent = translate(element.dataset.i18n);
  });

  document.querySelectorAll('[data-i18n-aria]').forEach((element) => {
    element.setAttribute('aria-label', translate(element.dataset.i18nAria));
  });

  document.querySelectorAll('[data-i18n-alt]').forEach((element) => {
    element.setAttribute('alt', translate(element.dataset.i18nAlt));
  });

  const langLabel = document.querySelector('[data-lang-label]');
  if (langLabel) {
    langLabel.textContent = translate('controls.nextLang');
  }
};

const applyTheme = (theme) => {
  const safeTheme = theme === 'light' ? 'light' : 'dark';
  root.dataset.theme = safeTheme;
  saveValue('theme', safeTheme);

  const themeButton = document.querySelector('[data-theme-toggle]');
  const themeLabel = document.querySelector('[data-theme-label]');
  const themeIcon = themeButton ? themeButton.querySelector('i') : null;

  if (themeLabel) {
    themeLabel.textContent = safeTheme === 'light' ? translate('controls.light') : translate('controls.dark');
  }

  if (themeIcon) {
    themeIcon.className = safeTheme === 'light' ? 'fa-solid fa-sun' : 'fa-solid fa-moon';
  }
};

applyLanguage(getSaved('lang', root.dataset.lang || 'en'));
applyTheme(getSaved('theme', root.dataset.theme || 'dark'));

const themeToggle = document.querySelector('[data-theme-toggle]');
if (themeToggle) {
  themeToggle.addEventListener('click', () => {
    applyTheme(getCurrentTheme() === 'dark' ? 'light' : 'dark');
  });
}

const langToggle = document.querySelector('[data-lang-toggle]');
if (langToggle) {
  langToggle.addEventListener('click', () => {
    applyLanguage(getCurrentLang() === 'en' ? 'ar' : 'en');
    applyTheme(getCurrentTheme());
  });
}

const siteLoader = document.getElementById('siteLoader');

if (siteLoader) {
  const loaderTitle = siteLoader.querySelector('[data-loader-title]');
  const loaderStatus = siteLoader.querySelector('[data-loader-status]');
  const loaderProgress = siteLoader.querySelector('[data-loader-progress]');
  const loaderCount = siteLoader.querySelector('[data-loader-count]');
  const loaderStartedAt = performance.now();
  const minLoaderTime = 1800;
  let loaderValue = 0;
  let loaderDone = false;

  const getLoaderMessages = () => [
    translate('loader.status1'),
    translate('loader.status2'),
    translate('loader.status3'),
    translate('loader.status4')
  ];

  const setLoaderProgress = (value) => {
    const safeValue = Math.max(0, Math.min(100, Math.round(value)));

    if (loaderProgress) {
      loaderProgress.style.width = `${safeValue}%`;
    }

    if (loaderCount) {
      loaderCount.textContent = `${String(safeValue).padStart(2, '0')}%`;
    }

    if (loaderStatus) {
      const messages = getLoaderMessages();
      loaderStatus.textContent = messages[Math.min(messages.length - 1, Math.floor(safeValue / 28))];
    }
  };

  const loaderTimer = window.setInterval(() => {
    loaderValue = Math.min(94, loaderValue + Math.random() * 7 + 5);
    setLoaderProgress(loaderValue);
  }, 140);

  const finishLoader = () => {
    if (loaderDone) return;
    loaderDone = true;

    window.clearInterval(loaderTimer);
    setLoaderProgress(100);

    if (loaderStatus) {
      loaderStatus.textContent = translate('loader.ready');
    }

    if (loaderTitle) {
      loaderTitle.textContent = translate('loader.welcome');
    }

    siteLoader.classList.add('is-complete');

    window.setTimeout(() => {
      siteLoader.classList.add('is-leaving');
      document.body.classList.remove('is-loading');

      window.setTimeout(() => {
        siteLoader.remove();
      }, 760);
    }, 650);
  };

  const scheduleLoaderFinish = () => {
    const remainingTime = Math.max(0, minLoaderTime - (performance.now() - loaderStartedAt));
    window.setTimeout(finishLoader, remainingTime);
  };

  setLoaderProgress(6);

  if (document.readyState === 'complete') {
    scheduleLoaderFinish();
  } else {
    window.addEventListener('load', scheduleLoaderFinish, { once: true });
  }

  window.setTimeout(finishLoader, 5500);
}

const yearElement = document.getElementById('year');
if (yearElement) {
  yearElement.textContent = new Date().getFullYear();
}

const revealElements = document.querySelectorAll('.reveal');
if ('IntersectionObserver' in window) {
  const observer = new IntersectionObserver((entries) => {
    entries.forEach((entry) => {
      if (entry.isIntersecting) {
        entry.target.classList.add('visible');
        observer.unobserve(entry.target);
      }
    });
  }, { threshold: 0.12 });

  revealElements.forEach((element, index) => {
    element.style.setProperty('--delay', `${Math.min(index * 0.06, 0.24)}s`);
    observer.observe(element);
  });
} else {
  revealElements.forEach((element) => element.classList.add('visible'));
}

const tiltCard = document.querySelector('[data-tilt-card]');

if (tiltCard && !prefersReducedMotion) {
  tiltCard.addEventListener('pointermove', (event) => {
    const rect = tiltCard.getBoundingClientRect();
    const x = (event.clientX - rect.left) / rect.width - 0.5;
    const y = (event.clientY - rect.top) / rect.height - 0.5;

    tiltCard.style.transform = `perspective(900px) rotateX(${y * -5}deg) rotateY(${x * 6}deg) translateY(-2px)`;
  });

  tiltCard.addEventListener('pointerleave', () => {
    tiltCard.style.transform = 'perspective(900px) rotateX(0deg) rotateY(0deg) translateY(0)';
  });
}

const goTopButton = document.querySelector('.go-top-btn');

if (goTopButton) {
  const toggleGoTopButton = () => {
    goTopButton.classList.toggle('show', window.scrollY > 420);
  };

  toggleGoTopButton();
  window.addEventListener('scroll', toggleGoTopButton, { passive: true });

  goTopButton.addEventListener('click', () => {
    window.scrollTo({
      top: 0,
      behavior: prefersReducedMotion ? 'auto' : 'smooth'
    });
  });
}

const supportsCustomCursor = window.matchMedia('(hover: hover) and (pointer: fine)').matches;
const cursorDot = document.querySelector('.cursor-dot');
const cursorGlow = document.querySelector('.cursor-glow');

if (supportsCustomCursor && !prefersReducedMotion && cursorDot && cursorGlow) {
  document.body.classList.add('has-custom-cursor');

  let mouseX = -100;
  let mouseY = -100;
  let glowX = mouseX;
  let glowY = mouseY;

  const updateCursor = () => {
    glowX += (mouseX - glowX) * 0.16;
    glowY += (mouseY - glowY) * 0.16;

    cursorDot.style.transform = `translate3d(${mouseX}px, ${mouseY}px, 0) translate(-50%, -50%)`;
    cursorGlow.style.transform = `translate3d(${glowX}px, ${glowY}px, 0) translate(-50%, -50%)`;

    requestAnimationFrame(updateCursor);
  };

  document.addEventListener('pointermove', (event) => {
    mouseX = event.clientX;
    mouseY = event.clientY;
    document.body.classList.add('cursor-active');
  }, { passive: true });

  document.addEventListener('pointerleave', () => {
    document.body.classList.remove('cursor-active');
  });

  document.addEventListener('pointerover', (event) => {
    if (event.target.closest('a, button, input, textarea, select, [role="button"]')) {
      document.body.classList.add('cursor-hover');
    }
  });

  document.addEventListener('pointerout', (event) => {
    if (event.target.closest('a, button, input, textarea, select, [role="button"]')) {
      document.body.classList.remove('cursor-hover');
    }
  });

  updateCursor();
}
